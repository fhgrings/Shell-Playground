# ------------------------------------------------------------------------ #
#  Este programa irá printar o numero de * de acordo com as variaveis COMECA e ATE
#  Exemplos:
#      $ ./tarefa3.sh
#      Neste exemplo o script será executado no modo debug nível 1.
# ------------------------------------------------------------------------ #
# Histórico:
#
#   v1.0 09/07/2020, Felipe:
#       - Início do programa
# ------------------------------------------------------------------------ #
# Testado em:
#   bash 5.0.19
# ------------------------------------------------------------------------ #

# ------------------------------- VARIÁVEIS ----------------------------------------- #
COMECA=0 ATE=100;

# ------------------------------------------------------------------------ #

# ------------------------------- TESTES ----------------------------------------- #


# ------------------------------------------------------------------------ #

# ------------------------------- FUNÇÕES ----------------------------------------- #

# ------------------------------------------------------------------------ #

# ------------------------------- EXECUÇÃO ----------------------------------------- #
[ $COMECA -ge $ATE ] && exit 1

for i in $(seq $COMECA $ATE);do
  for j in $(seq $i $ATE);do
    printf "*";
  done;
  printf "\n";
done
